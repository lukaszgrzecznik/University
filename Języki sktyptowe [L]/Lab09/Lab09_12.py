# Nieoczekiwane wyniki obliczeń, uzasadnij otrzymane wyniki dla podanych przypadków:
# 12. Przy mierzeniu pewnej wielkości uzyskano wynik p = 32.65. Wiedząc, iż maksymalny błąd
# względny tego pomiaru wynosi 0.5% znaleźć przedział, w którym zawarta jest wielkość p.

# Błąd względny 0,5% oznacza, że rzeczywista wartość mieści się w przedziale:

# p ± (0,5% \* p)

# Obliczenia:
# - 0,5% = 0,005
# - błąd bezwzględny = 0,005 \* 32.65 = 0.16325

# Przedział:
# - Dolna granica: 32.65 - 0.16325 = 32.48675
# - Górna granica: 32.65 + 0.16325 = 32.81325

# Odpowiedź:
# Wielkość p zawiera się w przedziale [32.48675, 32.81325].